package instituto;

public class Rendimiento {

	/*Atributos de la Clase */
	private int calificacion;
	private String observacion;
	private String enlace_interes;
	
	/*Constructor de la Clase */
	public Rendimiento () {
		
	}

	/**
	 * @return the calificacion
	 */
	public int getCalificacion() {
		return calificacion;
	}

	/**
	 * @param calificacion the calificacion to set
	 */
	public void setCalificacion(int calificacion) {
		this.calificacion = calificacion;
	}

	/**
	 * @return the observacion
	 */
	public String getObservacion() {
		return observacion;
	}

	/**
	 * @param observacion the observacion to set
	 */
	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}

	/**
	 * @return the enlace_interes
	 */
	public String getEnlace_interes() {
		return enlace_interes;
	}

	/**
	 * @param enlace_interes the enlace_interes to set
	 */
	public void setEnlace_interes(String enlace_interes) {
		this.enlace_interes = enlace_interes;
	}
	
	
	
}
